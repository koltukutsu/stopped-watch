part of "record_cubit.dart";


abstract class RecordState extends Equatable {
  const RecordState();

  @override
  List<Object> get props => [];
}

class RecordIdleState extends RecordState {
  @override
  List<Object> get props => [];
}

class GotRecordsState extends RecordState {
  @override
  List<Object> get props => [];
}

class NoRecordsState extends RecordState {
  @override
  List<Object> get props => [];
}